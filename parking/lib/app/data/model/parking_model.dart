class ParkingLot {
  final String name;
  final String address;
  final double distance;
  final int availableSpots;
  final double pricePerHour;
  final String image;

  ParkingLot({
    required this.name,
    required this.address,
    required this.distance,
    required this.availableSpots,
    required this.pricePerHour,
    required this.image,
  });
}
